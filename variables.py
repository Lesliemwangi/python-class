# To declare a variable we type the name of the variable
# followed by an equal sign and the value we want to assign to it
# Python accepts numbers with a decimal point or whole numbers

age = 20
print(age)

price = 19.95
print(price)

# If we want to use multple words we use (_) to separate them and make them readable
# We can also use a variable to store a string
first_name = 'Leslie'
print(first_name)

